package com.itheima.Interface;

public class Student {
    public String name;
    public char sex;
    public int age;
    public int score;

    public Student() {
    }

    public Student(String name, char sex, int age, int score) {
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.score = score;
    }

}
